    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        AdminIn bw = new AdminIn("./Infoentretenimiento.txt");
        String visor;
        int cantp, anp;
        visor = I_v.getSelectedItem().toString();
        cantp = (int) I_c.getValue();
        anp = (int) I_a.getValue();
        Infoentretenimiento t = new Infoentretenimiento(cantp, anp, visor);
        bw.getInfo().add(t);
        try {
            bw.escribirArchivo();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error al guardar bateria");
        }
        I_c.setValue(0);
        I_a.setValue(0);
        I_v.setSelectedIndex(0);
    }//GEN-LAST:event_jButton6MouseClicked

    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        AdminI bw = new AdminI("./Interiores.txt");
        String mat, vol;
        int bot;
        mat = In_m.getText();
        vol = In_t.getSelectedItem().toString();
        bot = (int) In_c.getValue();
        Interior b = new Interior(mat, vol, bot);
        bw.getIn().add(b);
        try {
            bw.escribirArchivo();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error al guardar bateria");
        }
        In_m.setText("");
        In_t.setSelectedIndex(0);
        In_c.setValue(0);
    }//GEN-LAST:event_jButton7MouseClicked

